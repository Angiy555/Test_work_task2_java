/**
 * Toy
 */
public class Toy {

    int id;
    String name;
    int quantity;
    float chanceOfDrop;

    public Toy(int id, String name, int quantity, float chanceOfDrop){
        this.id = id;
        this.name = name;
        this.quantity = quantity;
        this.chanceOfDrop = chanceOfDrop;
    }
}